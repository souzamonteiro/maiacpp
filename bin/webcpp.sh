#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd -P)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd -P)"
PROJECTS_ROOT="$(cd "$REPO_ROOT/.." && pwd -P)"

COMPILER_JS="$REPO_ROOT/compiler/cpp-compiler.js"
PARSER_JS="$REPO_ROOT/compiler/cpp-parser.js"
COLLECTOR_JS="$REPO_ROOT/compiler/parse-tree-collector.js"

resolve_repo_tool() {
	local local_path="$1"
	local sibling_path="$2"
	if [[ -f "$local_path" ]]; then
		echo "$local_path"
		return 0
	fi
	if [[ -f "$sibling_path" ]]; then
		echo "$sibling_path"
		return 0
	fi
	return 1
}

MAIAC_COMPILER_JS="$(resolve_repo_tool "$REPO_ROOT/maiac/compiler/c-compiler.js" "$PROJECTS_ROOT/maiac/compiler/c-compiler.js" || true)"

usage() {
	cat <<'EOF'
Usage: webcpp.sh <input.cpp> [options]
   or: webcpp.sh --file <input.cpp> [options]

Options:
	--file FILE            Input C++ source file (legacy form).
	--ast-show             Print AST tree to stdout.
	--ast-xml-out FILE     Write AST as XML.
	--ast-json-out FILE    Write AST as JSON.
	--c-out FILE           Write transpiled C output.
	--wat-out FILE         Write WAT output via MaiaC from generated C.
	--wasm-out FILE        Write WASM output via MaiaC from generated C.
	--out-dir DIR          Output directory used with --all (default: ./out).
	--all                  Generate AST XML, AST JSON, C, WAT, and WASM.
	-h, --help             Show this help.

Examples:
	webcpp.sh ./compiler/test.cpp --ast-show
	webcpp.sh ./compiler/test.cpp --c-out ./out/test.c
	webcpp.sh --file ./compiler/test.cpp --all --out-dir ./out

Notes:
	- C output is generated by compiler/cpp-compiler.js.
	- WAT/WASM output is generated by MaiaC from MaiaCpp C output.
EOF
}

err() {
	echo "Error: $*" >&2
	exit 1
}

INPUT_FILE=""
OUT_DIR=""
AST_SHOW=0
AST_XML_OUT=""
AST_JSON_OUT=""
C_OUT=""
WAT_OUT=""
WASM_OUT=""
ALL_OUTPUTS=0

while [[ $# -gt 0 ]]; do
	case "$1" in
		--)
			shift
			break
			;;
		--file)
			[[ $# -ge 2 ]] || err "missing value for --file"
			INPUT_FILE="$2"
			shift 2
			;;
		--ast-show)
			AST_SHOW=1
			shift
			;;
		--ast-xml-out)
			[[ $# -ge 2 ]] || err "missing value for --ast-xml-out"
			AST_XML_OUT="$2"
			shift 2
			;;
		--ast-json-out)
			[[ $# -ge 2 ]] || err "missing value for --ast-json-out"
			AST_JSON_OUT="$2"
			shift 2
			;;
		--c-out)
			[[ $# -ge 2 ]] || err "missing value for --c-out"
			C_OUT="$2"
			shift 2
			;;
		--wat-out)
			[[ $# -ge 2 ]] || err "missing value for --wat-out"
			WAT_OUT="$2"
			shift 2
			;;
		--wasm-out)
			[[ $# -ge 2 ]] || err "missing value for --wasm-out"
			WASM_OUT="$2"
			shift 2
			;;
		--out-dir)
			[[ $# -ge 2 ]] || err "missing value for --out-dir"
			OUT_DIR="$2"
			shift 2
			;;
		--all)
			ALL_OUTPUTS=1
			shift
			;;
		-h|--help)
			usage
			exit 0
			;;
		-*)
			err "unknown option: $1"
			;;
		*)
			if [[ -z "$INPUT_FILE" ]]; then
				INPUT_FILE="$1"
				shift
			else
				err "unexpected positional argument: $1"
			fi
			;;
	esac
done

if [[ $# -gt 0 ]]; then
	if [[ -z "$INPUT_FILE" ]]; then
		INPUT_FILE="$1"
		shift
	fi
	[[ $# -eq 0 ]] || err "unexpected extra arguments: $*"
fi

[[ -n "$INPUT_FILE" ]] || {
	usage
	err "input file is required"
}

[[ -f "$COMPILER_JS" ]] || err "compiler not found: $COMPILER_JS"
[[ -f "$PARSER_JS" ]] || err "parser not found: $PARSER_JS"
[[ -f "$COLLECTOR_JS" ]] || err "collector not found: $COLLECTOR_JS"
[[ -f "$INPUT_FILE" ]] || err "input file not found: $INPUT_FILE"

if [[ -n "$WAT_OUT" || -n "$WASM_OUT" ]]; then
	[[ -f "$MAIAC_COMPILER_JS" ]] || err "MaiaC compiler not found: $MAIAC_COMPILER_JS"
fi

INPUT_FILE="$(cd "$(dirname "$INPUT_FILE")" && pwd -P)/$(basename "$INPUT_FILE")"
BASENAME="$(basename "$INPUT_FILE")"
STEM="${BASENAME%.*}"

if [[ "$ALL_OUTPUTS" -eq 1 ]]; then
	if [[ -z "$OUT_DIR" ]]; then
		OUT_DIR="./out"
	fi
	mkdir -p "$OUT_DIR"
	[[ -n "$AST_XML_OUT" ]] || AST_XML_OUT="$OUT_DIR/$STEM.ast.xml"
	[[ -n "$AST_JSON_OUT" ]] || AST_JSON_OUT="$OUT_DIR/$STEM.ast.json"
	[[ -n "$C_OUT" ]] || C_OUT="$OUT_DIR/$STEM.c"
	[[ -n "$WAT_OUT" ]] || WAT_OUT="$OUT_DIR/$STEM.wat"
	[[ -n "$WASM_OUT" ]] || WASM_OUT="$OUT_DIR/$STEM.wasm"
fi

if [[ -n "$AST_XML_OUT" ]]; then
	mkdir -p "$(dirname "$AST_XML_OUT")"
fi
if [[ -n "$AST_JSON_OUT" ]]; then
	mkdir -p "$(dirname "$AST_JSON_OUT")"
fi
if [[ -n "$C_OUT" ]]; then
	mkdir -p "$(dirname "$C_OUT")"
fi
if [[ -n "$WAT_OUT" ]]; then
	mkdir -p "$(dirname "$WAT_OUT")"
fi
if [[ -n "$WASM_OUT" ]]; then
	mkdir -p "$(dirname "$WASM_OUT")"
fi

if [[ "$AST_SHOW" -eq 1 || -n "$AST_XML_OUT" || -n "$AST_JSON_OUT" ]]; then
	AST_CMD=(node "$COMPILER_JS" "$INPUT_FILE")
	if [[ "$AST_SHOW" -eq 1 ]]; then
		AST_CMD+=(--ast-show)
	fi
	if [[ -n "$AST_XML_OUT" ]]; then
		AST_CMD+=(--ast-xml-out "$AST_XML_OUT")
	fi
	if [[ -n "$AST_JSON_OUT" ]]; then
		AST_CMD+=(--ast-json-out "$AST_JSON_OUT")
	fi
	"${AST_CMD[@]}"
fi

if [[ -n "$C_OUT" || -n "$WAT_OUT" || -n "$WASM_OUT" ]]; then
	CPP_C_OUT="$C_OUT"
	TEMP_C_OUT=""
	if [[ ( -n "$WAT_OUT" || -n "$WASM_OUT" ) && -z "$CPP_C_OUT" ]]; then
		TMP_DIR="$(mktemp -d)"
		trap '[[ -n "$TEMP_C_OUT" ]] && rm -f "$TEMP_C_OUT"; [[ -n "${TMP_DIR:-}" ]] && rm -rf "$TMP_DIR"' EXIT
		TEMP_C_OUT="$TMP_DIR/$STEM.c"
		CPP_C_OUT="$TEMP_C_OUT"
	fi

	if [[ -n "$CPP_C_OUT" ]]; then
		CMD=(node "$COMPILER_JS" "$INPUT_FILE" --output "$CPP_C_OUT")
		"${CMD[@]}"
	fi

	if [[ -n "$WAT_OUT" || -n "$WASM_OUT" ]]; then
		MAIAC_CMD=(node "$MAIAC_COMPILER_JS" --file "$CPP_C_OUT" --no-wat)
		if [[ -n "$WAT_OUT" ]]; then
			MAIAC_CMD+=(--wat-out "$WAT_OUT")
		fi
		if [[ -n "$WASM_OUT" ]]; then
			MAIAC_CMD+=(--wasm-out "$WASM_OUT")
		fi
		"${MAIAC_CMD[@]}"
	fi
fi

if [[ -z "$AST_XML_OUT" && -z "$AST_JSON_OUT" && "$AST_SHOW" -eq 0 && -z "$C_OUT" && -z "$WAT_OUT" && -z "$WASM_OUT" ]]; then
	node "$COMPILER_JS" "$INPUT_FILE"
fi

#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd -P)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd -P)"
PROJECTS_ROOT="$(cd "$REPO_ROOT/.." && pwd -P)"

COMPILER_JS="$REPO_ROOT/compiler/cpp-compiler.js"
PARSER_JS="$REPO_ROOT/compiler/cpp-parser.js"
COLLECTOR_JS="$REPO_ROOT/compiler/parse-tree-collector.js"

resolve_repo_tool() {
	local local_path="$1"
	local sibling_path="$2"
	if [[ -f "$local_path" ]]; then
		echo "$local_path"
		return 0
	fi
	if [[ -f "$sibling_path" ]]; then
		echo "$sibling_path"
		return 0
	fi
	return 1
}

# Prefer the sibling MaiaC workspace when available; fall back to vendored copy.
MAIAC_WEBC_JS="$(resolve_repo_tool "$PROJECTS_ROOT/maiac/tools/webc.js" "$REPO_ROOT/maiac/tools/webc.js" || true)"
MAIAC_ROOT=""
if [[ -n "$MAIAC_WEBC_JS" ]]; then
	MAIAC_ROOT="$(cd "$(dirname "$MAIAC_WEBC_JS")/.." && pwd -P)"
fi

copy_dist_wasm_libs() {
	local dist_dir="$1"
	local app_wasm_basename="$2"
	local copied=0
	local skipped=0

	mkdir -p "$dist_dir"

	# Copy helper over one lib directory without overwriting existing dist assets.
	copy_from_lib_dir() {
		local lib_dir="$1"
		local src
		local base
		if [[ ! -d "$lib_dir" ]]; then
			return 0
		fi
		for src in "$lib_dir"/*.wasm; do
			[[ -f "$src" ]] || continue
			base="$(basename "$src")"
			if [[ "$base" == "$app_wasm_basename" ]]; then
				skipped=$((skipped + 1))
				continue
			fi
			if [[ -f "$dist_dir/$base" ]]; then
				skipped=$((skipped + 1))
				continue
			fi
			cp -f "$src" "$dist_dir/$base"
			copied=$((copied + 1))
		done
	}

	if [[ -n "$MAIAC_ROOT" ]]; then
		copy_from_lib_dir "$MAIAC_ROOT/lib"
	fi
	copy_from_lib_dir "$REPO_ROOT/lib"

	echo "[webcpp] dist libs copied: $copied (skipped: $skipped)"
}

usage() {
	cat <<'EOF'
Usage: webcpp.sh <input.cpp> [options]
   or: webcpp.sh --file <input.cpp> [options]

Options:
	--file FILE            Input C++ source file (legacy form).
	--ast-show             Print AST tree to stdout.
	--ast-xml-out FILE     Write AST as XML.
	--ast-json-out FILE    Write AST as JSON.
	--c-out FILE           Write transpiled C output.
	--js-out FILE          Write JS wrapper output from MaiaC webc.
	--wat-out FILE         Write WAT output via MaiaC from generated C.
	--wasm-out FILE        Write WASM output via MaiaC from generated C.
	--out-dir DIR          Output directory used with --all (default: ./out).
	--wat                  Also emit WAT via MaiaC webc (at MaiaC output base).
	--no-validate          Skip WAT/WASM validation in MaiaC webc.
	--resolve-system-includes  Enable system include expansion in MaiaC webc.
	--no-system-includes   Disable system include expansion in MaiaC webc.
	--dist                 Create distributable folder via MaiaC webc.
	--dist-run             Create dist and run node runner via MaiaC webc.
	--name NAME            Dist app name (maps to MaiaC webc --name).
	                        Dist now also aggregates .wasm libs from maiac/lib and maiacpp/lib.
	--webc-out-base BASE   MaiaC webc output base (maps to -o BASE).
	--run                  Run generated WASM after build via MaiaC webc.
	--all                  Generate AST XML, AST JSON, C, WAT, and WASM.
	-h, --help             Show this help.

Examples:
	webcpp.sh ./compiler/test.cpp --ast-show
	webcpp.sh ./compiler/test.cpp --c-out ./out/test.c
	webcpp.sh ./compiler/test.cpp --wasm-out ./out/test.wasm --run
	webcpp.sh ./compiler/test.cpp --dist --out-dir ./dist --name test
	webcpp.sh --file ./compiler/test.cpp --all --out-dir ./out

Notes:
	- C output is generated by compiler/cpp-compiler.js.
	- WAT/WASM/JS/dist output is generated by MaiaC webc from MaiaCpp C output.
EOF
}

err() {
	echo "Error: $*" >&2
	exit 1
}

INPUT_FILE=""
OUT_DIR=""
AST_SHOW=0
AST_XML_OUT=""
AST_JSON_OUT=""
C_OUT=""
JS_OUT=""
WAT_OUT=""
WASM_OUT=""
ALL_OUTPUTS=0
WEBC_WAT=0
WEBC_VALIDATE=1
WEBC_RESOLVE_SYSTEM_INCLUDES=0
WEBC_DIST=0
WEBC_DIST_RUN=0
WEBC_DIST_NAME=""
WEBC_OUT_BASE=""
WEBC_RUN=0

while [[ $# -gt 0 ]]; do
	case "$1" in
		--)
			shift
			break
			;;
		--file)
			[[ $# -ge 2 ]] || err "missing value for --file"
			INPUT_FILE="$2"
			shift 2
			;;
		--ast-show)
			AST_SHOW=1
			shift
			;;
		--ast-xml-out)
			[[ $# -ge 2 ]] || err "missing value for --ast-xml-out"
			AST_XML_OUT="$2"
			shift 2
			;;
		--ast-json-out)
			[[ $# -ge 2 ]] || err "missing value for --ast-json-out"
			AST_JSON_OUT="$2"
			shift 2
			;;
		--c-out)
			[[ $# -ge 2 ]] || err "missing value for --c-out"
			C_OUT="$2"
			shift 2
			;;
		--js-out)
			[[ $# -ge 2 ]] || err "missing value for --js-out"
			JS_OUT="$2"
			shift 2
			;;
		--wat-out)
			[[ $# -ge 2 ]] || err "missing value for --wat-out"
			WAT_OUT="$2"
			shift 2
			;;
		--wasm-out)
			[[ $# -ge 2 ]] || err "missing value for --wasm-out"
			WASM_OUT="$2"
			shift 2
			;;
		--out-dir)
			[[ $# -ge 2 ]] || err "missing value for --out-dir"
			OUT_DIR="$2"
			shift 2
			;;
		--wat)
			WEBC_WAT=1
			shift
			;;
		--no-validate)
			WEBC_VALIDATE=0
			shift
			;;
		--resolve-system-includes)
			WEBC_RESOLVE_SYSTEM_INCLUDES=1
			shift
			;;
		--no-system-includes)
			WEBC_RESOLVE_SYSTEM_INCLUDES=0
			shift
			;;
		--dist)
			WEBC_DIST=1
			shift
			;;
		--dist-run)
			WEBC_DIST=1
			WEBC_DIST_RUN=1
			shift
			;;
		--name)
			[[ $# -ge 2 ]] || err "missing value for --name"
			WEBC_DIST_NAME="$2"
			shift 2
			;;
		--webc-out-base)
			[[ $# -ge 2 ]] || err "missing value for --webc-out-base"
			WEBC_OUT_BASE="$2"
			shift 2
			;;
		--run)
			WEBC_RUN=1
			shift
			;;
		--all)
			ALL_OUTPUTS=1
			shift
			;;
		-h|--help)
			usage
			exit 0
			;;
		-*)
			err "unknown option: $1"
			;;
		*)
			if [[ -z "$INPUT_FILE" ]]; then
				INPUT_FILE="$1"
				shift
			else
				err "unexpected positional argument: $1"
			fi
			;;
	esac
done

if [[ $# -gt 0 ]]; then
	if [[ -z "$INPUT_FILE" ]]; then
		INPUT_FILE="$1"
		shift
	fi
	[[ $# -eq 0 ]] || err "unexpected extra arguments: $*"
fi

[[ -n "$INPUT_FILE" ]] || {
	usage
	err "input file is required"
}

[[ -f "$COMPILER_JS" ]] || err "compiler not found: $COMPILER_JS"
[[ -f "$PARSER_JS" ]] || err "parser not found: $PARSER_JS"
[[ -f "$COLLECTOR_JS" ]] || err "collector not found: $COLLECTOR_JS"
[[ -f "$INPUT_FILE" ]] || err "input file not found: $INPUT_FILE"

if [[ -n "$WAT_OUT" || -n "$WASM_OUT" ]]; then
	[[ -f "$MAIAC_WEBC_JS" ]] || err "MaiaC webc not found: $MAIAC_WEBC_JS"
fi

if [[ "$WEBC_WAT" -eq 1 || "$WEBC_VALIDATE" -eq 0 || "$WEBC_RESOLVE_SYSTEM_INCLUDES" -eq 1 || "$WEBC_DIST" -eq 1 || "$WEBC_DIST_RUN" -eq 1 || -n "$WEBC_DIST_NAME" || -n "$WEBC_OUT_BASE" || "$WEBC_RUN" -eq 1 || -n "$JS_OUT" ]]; then
	[[ -f "$MAIAC_WEBC_JS" ]] || err "MaiaC webc not found: $MAIAC_WEBC_JS"
fi

INPUT_FILE="$(cd "$(dirname "$INPUT_FILE")" && pwd -P)/$(basename "$INPUT_FILE")"
BASENAME="$(basename "$INPUT_FILE")"
STEM="${BASENAME%.*}"

if [[ "$ALL_OUTPUTS" -eq 1 ]]; then
	if [[ -z "$OUT_DIR" ]]; then
		OUT_DIR="./out"
	fi
	mkdir -p "$OUT_DIR"
	[[ -n "$AST_XML_OUT" ]] || AST_XML_OUT="$OUT_DIR/$STEM.ast.xml"
	[[ -n "$AST_JSON_OUT" ]] || AST_JSON_OUT="$OUT_DIR/$STEM.ast.json"
	[[ -n "$C_OUT" ]] || C_OUT="$OUT_DIR/$STEM.c"
	[[ -n "$WAT_OUT" ]] || WAT_OUT="$OUT_DIR/$STEM.wat"
	[[ -n "$WASM_OUT" ]] || WASM_OUT="$OUT_DIR/$STEM.wasm"
fi

if [[ -n "$AST_XML_OUT" ]]; then
	mkdir -p "$(dirname "$AST_XML_OUT")"
fi
if [[ -n "$AST_JSON_OUT" ]]; then
	mkdir -p "$(dirname "$AST_JSON_OUT")"
fi
if [[ -n "$C_OUT" ]]; then
	mkdir -p "$(dirname "$C_OUT")"
fi
if [[ -n "$JS_OUT" ]]; then
	mkdir -p "$(dirname "$JS_OUT")"
fi
if [[ -n "$WAT_OUT" ]]; then
	mkdir -p "$(dirname "$WAT_OUT")"
fi
if [[ -n "$WASM_OUT" ]]; then
	mkdir -p "$(dirname "$WASM_OUT")"
fi

if [[ "$AST_SHOW" -eq 1 || -n "$AST_XML_OUT" || -n "$AST_JSON_OUT" ]]; then
	AST_CMD=(node "$COMPILER_JS" "$INPUT_FILE")
	if [[ "$AST_SHOW" -eq 1 ]]; then
		AST_CMD+=(--ast-show)
	fi
	if [[ -n "$AST_XML_OUT" ]]; then
		AST_CMD+=(--ast-xml-out "$AST_XML_OUT")
	fi
	if [[ -n "$AST_JSON_OUT" ]]; then
		AST_CMD+=(--ast-json-out "$AST_JSON_OUT")
	fi
	"${AST_CMD[@]}"
fi

if [[ -n "$C_OUT" || -n "$JS_OUT" || -n "$WAT_OUT" || -n "$WASM_OUT" || "$WEBC_WAT" -eq 1 || "$WEBC_VALIDATE" -eq 0 || "$WEBC_RESOLVE_SYSTEM_INCLUDES" -eq 1 || "$WEBC_DIST" -eq 1 || "$WEBC_DIST_RUN" -eq 1 || -n "$WEBC_DIST_NAME" || -n "$WEBC_OUT_BASE" || "$WEBC_RUN" -eq 1 ]]; then
	CPP_C_OUT="$C_OUT"
	TEMP_C_OUT=""
	if [[ ( -n "$JS_OUT" || -n "$WAT_OUT" || -n "$WASM_OUT" || "$WEBC_WAT" -eq 1 || "$WEBC_VALIDATE" -eq 0 || "$WEBC_RESOLVE_SYSTEM_INCLUDES" -eq 1 || "$WEBC_DIST" -eq 1 || "$WEBC_DIST_RUN" -eq 1 || -n "$WEBC_DIST_NAME" || -n "$WEBC_OUT_BASE" || "$WEBC_RUN" -eq 1 ) && -z "$CPP_C_OUT" ]]; then
		TMP_DIR="$(mktemp -d)"
		trap '[[ -n "$TEMP_C_OUT" ]] && rm -f "$TEMP_C_OUT"; [[ -n "${TMP_DIR:-}" ]] && rm -rf "$TMP_DIR"' EXIT
		TEMP_C_OUT="$TMP_DIR/$STEM.c"
		CPP_C_OUT="$TEMP_C_OUT"
	fi

	if [[ -n "$CPP_C_OUT" ]]; then
		CMD=(node "$COMPILER_JS" "$INPUT_FILE" --output "$CPP_C_OUT")
		"${CMD[@]}"
	fi

	if [[ -n "$JS_OUT" || -n "$WAT_OUT" || -n "$WASM_OUT" || "$WEBC_WAT" -eq 1 || "$WEBC_VALIDATE" -eq 0 || "$WEBC_RESOLVE_SYSTEM_INCLUDES" -eq 1 || "$WEBC_DIST" -eq 1 || "$WEBC_DIST_RUN" -eq 1 || -n "$WEBC_DIST_NAME" || -n "$WEBC_OUT_BASE" || "$WEBC_RUN" -eq 1 ]]; then
		WEB_OUT_BASE="$WEBC_OUT_BASE"
		if [[ -z "$WEB_OUT_BASE" ]]; then
			if [[ -n "$WASM_OUT" ]]; then
				WEB_OUT_BASE="${WASM_OUT%.*}"
			elif [[ -n "$WAT_OUT" ]]; then
				WEB_OUT_BASE="${WAT_OUT%.*}"
			elif [[ -n "$JS_OUT" ]]; then
				WEB_OUT_BASE="${JS_OUT%.*}"
			elif [[ -n "$C_OUT" ]]; then
				WEB_OUT_BASE="${C_OUT%.*}"
			else
				WEB_OUT_BASE="$(dirname "$CPP_C_OUT")/$STEM"
			fi
		fi

		mkdir -p "$(dirname "$WEB_OUT_BASE")"

		WEBC_CMD=(node "$MAIAC_WEBC_JS" "$CPP_C_OUT" -o "$WEB_OUT_BASE")
		if [[ "$WEBC_WAT" -eq 1 || -n "$WAT_OUT" ]]; then
			WEBC_CMD+=(--wat)
		fi
		if [[ "$WEBC_VALIDATE" -eq 0 ]]; then
			WEBC_CMD+=(--no-validate)
		fi
		if [[ "$WEBC_RESOLVE_SYSTEM_INCLUDES" -eq 1 ]]; then
			WEBC_CMD+=(--resolve-system-includes)
		else
			WEBC_CMD+=(--no-system-includes)
		fi
		if [[ "$WEBC_DIST_RUN" -eq 1 ]]; then
			WEBC_CMD+=(--dist-run)
		elif [[ "$WEBC_DIST" -eq 1 ]]; then
			WEBC_CMD+=(--dist)
		fi
		if [[ "$WEBC_DIST" -eq 1 || "$WEBC_DIST_RUN" -eq 1 ]]; then
			if [[ -n "$OUT_DIR" ]]; then
				WEBC_CMD+=(--out-dir "$OUT_DIR")
			fi
			if [[ -n "$WEBC_DIST_NAME" ]]; then
				WEBC_CMD+=(--name "$WEBC_DIST_NAME")
			fi
		fi
		if [[ "$WEBC_RUN" -eq 1 ]]; then
			WEBC_CMD+=(--run)
		fi

		"${WEBC_CMD[@]}"

		if [[ "$WEBC_DIST" -eq 1 || "$WEBC_DIST_RUN" -eq 1 ]]; then
			DIST_DIR="$OUT_DIR"
			if [[ -z "$DIST_DIR" ]]; then
				DIST_DIR="$PWD/dist"
			fi
			DIST_APP_NAME="$WEBC_DIST_NAME"
			if [[ -z "$DIST_APP_NAME" ]]; then
				DIST_APP_NAME="$(basename "$WEB_OUT_BASE")"
			fi
			copy_dist_wasm_libs "$DIST_DIR" "$DIST_APP_NAME.wasm"
		fi

		GEN_WASM="$WEB_OUT_BASE.wasm"
		GEN_WAT="$WEB_OUT_BASE.wat"
		GEN_JS="$WEB_OUT_BASE.js"

		if [[ -n "$WASM_OUT" && "$GEN_WASM" != "$WASM_OUT" ]]; then
			cp -f "$GEN_WASM" "$WASM_OUT"
		fi
		if [[ -n "$WAT_OUT" ]]; then
			[[ -f "$GEN_WAT" ]] || err "MaiaC webc did not emit WAT at: $GEN_WAT"
			if [[ "$GEN_WAT" != "$WAT_OUT" ]]; then
				cp -f "$GEN_WAT" "$WAT_OUT"
			fi
		fi
		if [[ -n "$JS_OUT" ]]; then
			if [[ "$GEN_JS" != "$JS_OUT" ]]; then
				cp -f "$GEN_JS" "$JS_OUT"
			fi
		fi
	fi
fi

if [[ -z "$AST_XML_OUT" && -z "$AST_JSON_OUT" && "$AST_SHOW" -eq 0 && -z "$C_OUT" && -z "$JS_OUT" && -z "$WAT_OUT" && -z "$WASM_OUT" && "$WEBC_WAT" -eq 0 && "$WEBC_VALIDATE" -eq 1 && "$WEBC_RESOLVE_SYSTEM_INCLUDES" -eq 0 && "$WEBC_DIST" -eq 0 && "$WEBC_DIST_RUN" -eq 0 && -z "$WEBC_DIST_NAME" && -z "$WEBC_OUT_BASE" && "$WEBC_RUN" -eq 0 ]]; then
	node "$COMPILER_JS" "$INPUT_FILE"
fi

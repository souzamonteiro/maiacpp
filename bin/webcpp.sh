#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd -P)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd -P)"

COMPILER_JS="$REPO_ROOT/compiler/cpp-compiler.js"
PARSER_JS="$REPO_ROOT/compiler/cpp-parser.js"
COLLECTOR_JS="$REPO_ROOT/compiler/parse-tree-collector.js"
RUNTIME_WAT="$REPO_ROOT/compiler/runtime.wat"

usage() {
	cat <<'EOF'
Usage: webcpp.sh --file input.cpp [options]

Options:
	--file FILE            Input C++ source file.
	--ast-show             Print AST tree to stdout.
	--ast-xml-out FILE     Write AST as XML.
	--ast-json-out FILE    Write AST as JSON.
	--c-out FILE           Write transpiled C output.
	--wat-out FILE         Write WAT output (currently runtime.wat template).
	--wasm-out FILE        Write WASM output (compiled from WAT via wat2wasm).
	--out-dir DIR          Output directory used with --all (default: ./out).
	--all                  Generate AST XML, AST JSON, C, WAT, and WASM.
	-h, --help             Show this help.

Examples:
	webcpp.sh --file ./compiler/test.cpp --ast-show
	webcpp.sh --file ./compiler/test.cpp --c-out ./out/test.c
	webcpp.sh --file ./compiler/test.cpp --all --out-dir ./out

Notes:
	- C output is generated by compiler/cpp-compiler.js.
	- WAT output uses compiler/runtime.wat until a dedicated C->WAT backend is added.
EOF
}

err() {
	echo "Error: $*" >&2
	exit 1
}

INPUT_FILE=""
OUT_DIR=""
AST_SHOW=0
AST_XML_OUT=""
AST_JSON_OUT=""
C_OUT=""
WAT_OUT=""
WASM_OUT=""
ALL_OUTPUTS=0

while [[ $# -gt 0 ]]; do
	case "$1" in
		--file)
			[[ $# -ge 2 ]] || err "missing value for --file"
			INPUT_FILE="$2"
			shift 2
			;;
		--ast-show)
			AST_SHOW=1
			shift
			;;
		--ast-xml-out)
			[[ $# -ge 2 ]] || err "missing value for --ast-xml-out"
			AST_XML_OUT="$2"
			shift 2
			;;
		--ast-json-out)
			[[ $# -ge 2 ]] || err "missing value for --ast-json-out"
			AST_JSON_OUT="$2"
			shift 2
			;;
		--c-out)
			[[ $# -ge 2 ]] || err "missing value for --c-out"
			C_OUT="$2"
			shift 2
			;;
		--wat-out)
			[[ $# -ge 2 ]] || err "missing value for --wat-out"
			WAT_OUT="$2"
			shift 2
			;;
		--wasm-out)
			[[ $# -ge 2 ]] || err "missing value for --wasm-out"
			WASM_OUT="$2"
			shift 2
			;;
		--out-dir)
			[[ $# -ge 2 ]] || err "missing value for --out-dir"
			OUT_DIR="$2"
			shift 2
			;;
		--all)
			ALL_OUTPUTS=1
			shift
			;;
		-h|--help)
			usage
			exit 0
			;;
		*)
			err "unknown option: $1"
			;;
	esac
done

[[ -n "$INPUT_FILE" ]] || {
	usage
	err "--file is required"
}

[[ -f "$COMPILER_JS" ]] || err "compiler not found: $COMPILER_JS"
[[ -f "$PARSER_JS" ]] || err "parser not found: $PARSER_JS"
[[ -f "$COLLECTOR_JS" ]] || err "collector not found: $COLLECTOR_JS"
[[ -f "$RUNTIME_WAT" ]] || err "runtime WAT not found: $RUNTIME_WAT"
[[ -f "$INPUT_FILE" ]] || err "input file not found: $INPUT_FILE"

INPUT_FILE="$(cd "$(dirname "$INPUT_FILE")" && pwd -P)/$(basename "$INPUT_FILE")"
BASENAME="$(basename "$INPUT_FILE")"
STEM="${BASENAME%.*}"

if [[ "$ALL_OUTPUTS" -eq 1 ]]; then
	if [[ -z "$OUT_DIR" ]]; then
		OUT_DIR="./out"
	fi
	mkdir -p "$OUT_DIR"
	[[ -n "$AST_XML_OUT" ]] || AST_XML_OUT="$OUT_DIR/$STEM.ast.xml"
	[[ -n "$AST_JSON_OUT" ]] || AST_JSON_OUT="$OUT_DIR/$STEM.ast.json"
	[[ -n "$C_OUT" ]] || C_OUT="$OUT_DIR/$STEM.c"
	[[ -n "$WAT_OUT" ]] || WAT_OUT="$OUT_DIR/$STEM.wat"
	[[ -n "$WASM_OUT" ]] || WASM_OUT="$OUT_DIR/$STEM.wasm"
fi

if [[ -n "$AST_XML_OUT" ]]; then
	mkdir -p "$(dirname "$AST_XML_OUT")"
fi
if [[ -n "$AST_JSON_OUT" ]]; then
	mkdir -p "$(dirname "$AST_JSON_OUT")"
fi
if [[ -n "$C_OUT" ]]; then
	mkdir -p "$(dirname "$C_OUT")"
fi
if [[ -n "$WAT_OUT" ]]; then
	mkdir -p "$(dirname "$WAT_OUT")"
fi
if [[ -n "$WASM_OUT" ]]; then
	mkdir -p "$(dirname "$WASM_OUT")"
fi

if [[ "$AST_SHOW" -eq 1 || -n "$AST_XML_OUT" || -n "$AST_JSON_OUT" ]]; then
	node - "$INPUT_FILE" "$PARSER_JS" "$COLLECTOR_JS" "$AST_XML_OUT" "$AST_JSON_OUT" "$AST_SHOW" <<'NODE'
const fs = require('fs');

const [,, inputPath, parserPath, collectorPath, astXmlOut, astJsonOut, astShow] = process.argv;

const Parser = require(parserPath);
const { ParseTreeCollector, printTree } = require(collectorPath);

const source = fs.readFileSync(inputPath, 'utf8');
const collector = new ParseTreeCollector();

try {
	const parser = new Parser(source, collector);
	parser.parse();
} catch (err) {
	console.error(`AST generation failed: ${err.message}`);
	process.exit(2);
}

if (!collector.root) {
	console.error('AST generation failed: parse tree is empty');
	process.exit(2);
}

if (astXmlOut) {
	fs.writeFileSync(astXmlOut, collector.toXml({ includeDeclaration: true }), 'utf8');
	console.log(`AST XML written: ${astXmlOut}`);
}

if (astJsonOut) {
	fs.writeFileSync(astJsonOut, collector.toJSON(2), 'utf8');
	console.log(`AST JSON written: ${astJsonOut}`);
}

if (astShow === '1') {
	printTree(collector.root);
}
NODE
fi

if [[ -n "$C_OUT" ]]; then
	node "$COMPILER_JS" "$INPUT_FILE" --output "$C_OUT"
fi

if [[ -n "$WAT_OUT" ]]; then
	cp "$RUNTIME_WAT" "$WAT_OUT"
	echo "WAT written: $WAT_OUT"
fi

if [[ -n "$WASM_OUT" ]]; then
	WAT_SOURCE="$WAT_OUT"
	if [[ -z "$WAT_SOURCE" ]]; then
		WAT_SOURCE="$(mktemp "/tmp/${STEM}.XXXXXX.wat")"
		cp "$RUNTIME_WAT" "$WAT_SOURCE"
	fi

	if ! command -v wat2wasm >/dev/null 2>&1; then
		err "wat2wasm not found in PATH; required for --wasm-out"
	fi

	wat2wasm "$WAT_SOURCE" -o "$WASM_OUT"
	echo "WASM written: $WASM_OUT"
fi

if [[ -z "$AST_XML_OUT" && -z "$AST_JSON_OUT" && "$AST_SHOW" -eq 0 && -z "$C_OUT" && -z "$WAT_OUT" && -z "$WASM_OUT" ]]; then
	node "$COMPILER_JS" "$INPUT_FILE"
fi
